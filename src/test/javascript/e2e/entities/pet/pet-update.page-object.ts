import { element, by, ElementFinder } from 'protractor';
import { waitUntilDisplayed, waitUntilHidden, isVisible } from '../../util/utils';

const expect = chai.expect;

export default class PetUpdatePage {
  pageTitle: ElementFinder = element(by.id('petStoreApp.pet.home.createOrEditLabel'));
  saveButton: ElementFinder = element(by.id('save-entity'));
  cancelButton: ElementFinder = element(by.id('cancel-save'));
  nameInput: ElementFinder = element(by.css('input#pet-name'));
  statusSelect: ElementFinder = element(by.css('select#pet-status'));
  categorySelect: ElementFinder = element(by.css('select#pet-category'));
  tagsSelect: ElementFinder = element(by.css('select#pet-tags'));

  getPageTitle() {
    return this.pageTitle;
  }

  async setNameInput(name) {
    await this.nameInput.sendKeys(name);
  }

  async getNameInput() {
    return this.nameInput.getAttribute('value');
  }

  async setStatusSelect(status) {
    await this.statusSelect.sendKeys(status);
  }

  async getStatusSelect() {
    return this.statusSelect.element(by.css('option:checked')).getText();
  }

  async statusSelectLastOption() {
    await this.statusSelect.all(by.tagName('option')).last().click();
  }
  async categorySelectLastOption() {
    await this.categorySelect.all(by.tagName('option')).last().click();
  }

  async categorySelectOption(option) {
    await this.categorySelect.sendKeys(option);
  }

  getCategorySelect() {
    return this.categorySelect;
  }

  async getCategorySelectedOption() {
    return this.categorySelect.element(by.css('option:checked')).getText();
  }

  async tagsSelectLastOption() {
    await this.tagsSelect.all(by.tagName('option')).last().click();
  }

  async tagsSelectOption(option) {
    await this.tagsSelect.sendKeys(option);
  }

  getTagsSelect() {
    return this.tagsSelect;
  }

  async getTagsSelectedOption() {
    return this.tagsSelect.element(by.css('option:checked')).getText();
  }

  async save() {
    await this.saveButton.click();
  }

  async cancel() {
    await this.cancelButton.click();
  }

  getSaveButton() {
    return this.saveButton;
  }

  async enterData() {
    await waitUntilDisplayed(this.saveButton);
    await this.setNameInput('name');
    expect(await this.getNameInput()).to.match(/name/);
    await waitUntilDisplayed(this.saveButton);
    await this.statusSelectLastOption();
    await this.categorySelectLastOption();
    // this.tagsSelectLastOption();
    await this.save();
    await waitUntilHidden(this.saveButton);
    expect(await isVisible(this.saveButton)).to.be.false;
  }
}
